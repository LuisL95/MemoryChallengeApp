    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

  
        // TODO add your handling code here:
        for(int i = 0; i<fp.fb.listaBotones.size();i++)
        {
            
            
                if(fp.fb.listaBotones.get(i).getLabel().equals(this.codigoFrameEnunciado))
                {
                    fp.fb.listaBotones.get(i).setVisible(false);
                    fp.fb.listaBotones.remove(i);
                    fp.fb.contadorEnunciado = fp.fb.contadorEnunciado-1;
                    
                    fp.fb.bloque.enunciados.remove(busquedaEnunciado());
         for(int e = 0; e <fp.fb.bloque.enunciados.size(); e++ )
        {
            System.out.println(fp.fb.bloque.enunciados.get(e).getCodigoEnunciado());
        }

                    this.dispose();
                }
        }
    }//GEN-LAST:event_jButton2MouseClicked

