    int busquedaEnunciado ()
    {
    for(int i = 0; i < fp.fb.bloque.enunciados.size(); i++)
        {
            if(fp.fb.bloque.enunciados.get(i).getCodigoEnunciado().equals(this.codigoFrameEnunciado))
            {
                numEnunciado = i;
               
            }
            
            
        }
     return numEnunciado;
    }
    
